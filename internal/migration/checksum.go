package migration

import (
	"crypto/sha256"
	"encoding/hex"
	"strings"
)

// Checksum возвращает sha256 (hex) НОРМАЛИЗОВАННОГО содержимого миграции для
// ledger'а: нормализуются переводы строк (CRLF/CR -> LF) и обрезаются крайние
// пробелы всего содержимого — так переключение стиля переводов строк или лишняя
// пустая строка в конце файла не меняют сумму. ЛЮБОЕ другое изменение SQL сумму
// меняет, включая значимый пробел внутри строкового/dollar-quoted литерала или
// тела функции. Это «нормализованная», а не «голая» сумма; цель — поймать
// изменённую миграцию, переприменяемую под тем же именем (ложное «изменилась»
// безопаснее ложного «та же»).
func Checksum(content string) string {
	sum := sha256.Sum256([]byte(normalizeForChecksum(content)))
	return hex.EncodeToString(sum[:])
}

func normalizeForChecksum(s string) string {
	s = strings.ReplaceAll(s, "\r\n", "\n")
	s = strings.ReplaceAll(s, "\r", "\n")
	// НЕ обрезаем хвостовые пробелы построчно: внутри строкового/dollar-quoted
	// литерала или тела функции такой пробел значим, и его потеря дала бы коллизию
	// сумм для семантически разных миграций. Достаточно нормализовать переводы
	// строк и крайние пробелы всего содержимого.
	return strings.TrimSpace(s)
}
